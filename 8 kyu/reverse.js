/*
Complete the solution so that it reverses the string passed into it.
'world'  =>  'dlrow'
'word'   =>  'drow'
*/

//The split() method splits a string into an array of substrings
//The reverse() method reverses the order of elements in the array of substrings created by split()
//The join() method combines the elements of the array reversed by reverse() and stores it as a string value

solution = (str) => str.split('').reverse().join('')




